const a = 7
let b = 3

// O SÍMBOLO DA OPERAÇÃO SEMPRE VEM PRMEIRO
// atribuição de soma
b += a // b = b + a // pega b soma com a 
console.log(b)

// atribuição de subtração
b -= 4 // b = b -4 // 6
console.log(b)

// atribuição de multiplicação
b *= 2 // b = b * a
console.log(b)


// atribuição de divisão
b /= 2 // b = b / a
console.log(b)

b %= 2 // b = b % 2
console.log(b)